if (!password) { console.error("Set FIREBASE_PASSWORD env var"); process.exit(1); }

async function migrate() {
  const firebaseApp = initializeApp(firebaseConfig);
  const db = getFirestore(firebaseApp);
  const auth = getAuth(firebaseApp);

  console.log("Signing in...");
  await signInWithEmailAndPassword(auth, "start@najahchemist.com", password);
  console.log("Signed in. Migrating 58 products...\n");

  let ok = 0, fail = 0;
  for (const product of PRODUCTS) {
    try {
      await setDoc(doc(db, 'products', product.id), product);
      console.log(`  ✓ ${product.id}  ${product.name}`);
      ok++;
    } catch (e) {
      console.error(`  ✗ ${product.id}  ${product.name}: ${e.message}`);
      fail++;
    }
  }

  console.log(`\nDone: ${ok} saved, ${fail} failed.`);
  process.exit(fail > 0 ? 1 : 0);
}

